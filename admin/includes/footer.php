                if (footerForm) {
                  footerForm.addEventListener('submit', function(e) {
                    e.preventDefault();
                    const email = document.getElementById('footerNewsletterEmail').value;
                    const messageDiv = document.getElementById('footerNewsletterMessage');
                    
                    if (!email || !email.includes('@')) {
                      messageDiv.innerHTML = '<span style="color: #ff6b6b;">Zadejte platný email</span>';
                      messageDiv.style.display = 'block';
                      return;
                    }
                    
                    fetch('ajax/newsletter_signup.php', {
                      method: 'POST',
                      headers: { 'Content-Type': 'application/json' },
                      body: JSON.stringify({ email: email })
                    })
                    .then(response => response.json())
                    .then(data => {
                      if (data.success) {
                        messageDiv.innerHTML = '<span style="color: #4caf50;">✓ ' + data.message + ' Pokud e-mail nepřijde do pár minut, zkontrolujte prosím složku Spam/Promo.</span>';
                        footerForm.reset();
                        setTimeout(() => {
                          messageDiv.style.display = 'none';
                        }, 5000);
                      } else {
                        messageDiv.innerHTML = '<span style="color: #ff6b6b;">✗ ' + data.message + '</span>';
                      }
                      messageDiv.style.display = 'block';
                    })
                    .catch(error => {
                      console.error('Error:', error);
                      messageDiv.innerHTML = '<span style="color: #ff6b6b;">Chyba při odesílání</span>';
                      messageDiv.style.display = 'block';
                    });
                  });
                }
